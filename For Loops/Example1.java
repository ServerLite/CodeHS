public class ForLoop extends ConsoleProgram {
	public void run() {
	
		// Looping 10 times
		
		// int i = 0;          Starting point for the for loop
		// i < 10;             Looping until 'i' reachs 10
		// i++                 'i' increases by one
		for (int i = 0; i < 10; i++) {
			System.out.println(i); // Displaying the variable 'i' from the for loop to the console
		};
	};
};
